package org.cct.cp2019a.treasurehunt.enumeration;

/**
 * The allowed game actions.
 */
public enum GameAction {
    // should fill the number of players
    FILL_NUMBER_OF_PLAYERS,
    // should fill the player's name
    FILL_PROFILE_NAME,
    // should fill the player's age
    FILL_PROFILE_AGE,
    // should dig the game board
    DIG,
    // should try to buy a new shovel
    BUY_SHOVEL
}
