package org.cct.cp2019a.treasurehunt.view;

public class DigAction {

    private int player;
    private String col;
    private int row;

    public DigAction() {}

    public int getPlayer() {
        return player;
    }

    public void setPlayer(int player) {
        this.player = player;
    }

    public String getCol() {
        return col;
    }

    public void setCol(String col) {
        this.col = col;
    }

    public int getRow() {
        return row;
    }

    public void setRow(int row) {
        this.row = row;
    }
}
